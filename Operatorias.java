public class Operatorias{
    public static void main(String args[]){
        int numero_uno = 4;
        int numero_dos = 4;
        int res = 0;

        res = numero_uno + numero_dos;
        System.out.println("El resultado es:: "+res);

        res = numero_uno - numero_dos;
        System.out.println("El resultado es:: "+res);

        res = numero_uno * numero_dos;
        System.out.println("El resultado es:: "+res);

        res = numero_uno / numero_dos;
        System.out.println("El resultado es:: "+res);



    }
}
